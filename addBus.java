        public void atuoId() {
            try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(id) from bus");
            rs.next();
            rs.getString("MAX(id)");
            if (rs.getString("MAX(id)") == null) {
            flid.setText("B001");
            
            } else {
            long id = Long.parseLong(rs.getString("MAX(id)").substring(2, rs.getString("MAX(id)").length()));
            id++;
            flid.setText("B" + String.format("%03d", id));
            }}   
            catch (ClassNotFoundException ex) {
            Logger.getLogger(addBus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(addBus.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }
        
