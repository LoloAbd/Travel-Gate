    private void cotxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cotxtActionPerformed
        // TODO add your handling code here:
         String coun = cotxt.getSelectedItem().toString().trim();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            pst = con.prepareStatement("select * from road where countries = ?");
            pst.setString(1, coun);
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            
            int c;
            c = rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)Table.getModel();
            df.setRowCount(0);
            
            
            while(rs.next()){
                Vector v = new Vector();
                
                for(int i=1 ; i<=c ; i++){
                    v.add(rs.getString("source"));
                    v.add(rs.getString("departure"));
                    v.add(rs.getString("note"));
                }
                
                df.addRow(v);
            }
            
        }
        catch (ClassNotFoundException ex) {
            Logger.getLogger(TicketAirline.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TicketAirline.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cotxtActionPerformed

