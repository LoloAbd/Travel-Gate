    private void FindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindActionPerformed
        // TODO add your handling code here:
        String id = adid.getText();
        try {

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            pst = con.prepareStatement("Select * from admin where id = ? ");
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, " Not Found :( ");
            }
            else{
                String fname = rs.getString("firstname");
                String lname = rs.getString("lastname");
                String un = rs.getString("username");
                String pw = rs.getString("password");
                ftxt.setText(fname.trim());
                ltxt.setText(lname.trim());
                vtxt.setText(un.trim());
                pptxt.setText(pw.trim());
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(searchAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(searchAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_FindActionPerformed

