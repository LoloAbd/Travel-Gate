        // TODO add your handling code here:
        String id = adid.getText();
        String firstName = ftxt.getText();
        String lastName = ltxt.getText();
        String un = vtxt.getText();
        String pw = pptxt.getText();
       
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            pst = con.prepareStatement("update admin set firstname = ?,lastname = ?,username = ?,password = ? where id = ?");
            pst.setString(1, firstName);
            pst.setString(2, lastName);
            pst.setString(3, un);
            pst.setString(4, pw);
            pst.setString(5, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, " Updated (: ");
        }
        catch (ClassNotFoundException ex) {
            Logger.getLogger(searchAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(searchAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.hide();
