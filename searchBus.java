    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // TODO add your handling code here:
        String id = busid.getText();
        try {

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            pst = con.prepareStatement("Select * from bus where ID = ? ");
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, " Not Found :( ");
            }
            else{
                String bname = rs.getString("busname");
                String contr = rs.getString("countries");
                String source = rs.getString("source");
                String dep = rs.getString("departure");
                String not = rs.getString("note");
                String dateb = rs.getString("date");
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dateb);
                String depT = rs.getString("departime");
                String arT = rs.getString("arrtime");
                String bsCh = rs.getString("buscharge");
                
                
                bntxt.setText(bname.trim());
                cotxt.setText(contr.trim());
                sotxt.setText(source.trim());
                dptxt.setText(dep.trim());
                nttxt.setText(not.trim());
                dtxt.setDate(date);
                dttxt.setText(depT.trim());
                attxt.setText(arT.trim());
                bctxt.setText(bsCh.trim());
           

            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(searchBus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(searchBus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(searchBus.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_findActionPerformed

