    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String id = flid.getText();
        String flightName = ftxt.getText();
        String source = stxt.getText();
        String Departure  = deptxt.getText();
        DateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
        String date = dt.format(dtxt.getDate());
        String dT = dttxt.getText();
        String aT  = attxt.getText();
        String flightCh = fctxt.getText();
     
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            pst = con.prepareStatement("update flight set flightname=?,source=?,departure=?,date=?,departime=?,arrtime=?,flightcharge=? where ID=? ");
            pst.setString(1, flightName);
            pst.setString(2, source);
            pst.setString(3, Departure);
            pst.setString(4, date);
            pst.setString(5, dT);
            pst.setString(6, aT);
            pst.setString(7, flightCh);
            pst.setString(8, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, " Flight Updated (: ");
        }
        catch (ClassNotFoundException ex) {
            Logger.getLogger(searchFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(searchFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.hide();

    }//GEN-LAST:event_updateActionPerformed

