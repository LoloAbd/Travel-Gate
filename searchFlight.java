    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // TODO add your handling code here:
        String id = flid.getText();
        try {

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?serverTimezone=UTC", "root", "");
            pst = con.prepareStatement("Select * from flight where ID = ? ");
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, " Not Found :( ");
            }
            else{
                String fname = rs.getString("flightname");
                String source = rs.getString("source");
                String dep = rs.getString("departure");
                String datef = rs.getString("date");
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(datef);
                String depT = rs.getString("departime");
                String arT = rs.getString("arrtime");
                String flCh = rs.getString("flightcharge");
                
                
                ftxt.setText(fname.trim());
                stxt.setText(source.trim());
                deptxt.setText(dep.trim());
                dttxt.setText(depT.trim());
                attxt.setText(arT.trim());
                dtxt.setDate(date);
                fctxt.setText(flCh.trim());

            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(searchFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(searchFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(searchFlight.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_findActionPerformed

